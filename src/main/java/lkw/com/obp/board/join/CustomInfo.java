package lkw.com.obp.board.join;

public class CustomInfo {
    private String userId;
    private String userName;

    public String getUserId() {
        return userId;
    }

    public CustomInfo setUserId(String userId) {
        this.userId = userId;
        return this;
    }

    public String getUserName() {
        return userName;
    }

    public CustomInfo setUserName(String userName) {
        this.userName = userName;
        return this;
    }
}
